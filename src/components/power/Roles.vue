<template>
    <div>
      <!--面包屑导航区-->
      <el-breadcrumb separator="/">
        <el-breadcrumb-item :to="{ path: '/' }">首页</el-breadcrumb-item>
        <el-breadcrumb-item><a href="/">权限管理</a></el-breadcrumb-item>
        <el-breadcrumb-item>角色列表</el-breadcrumb-item>
      </el-breadcrumb>
      <!--添加搜索卡-->
      <el-card>
        <el-row>
          <el-col>
            <el-button type="primary" size="mini">添加角色</el-button>
          </el-col>
        </el-row>
      </el-card>
      <el-card>
        <el-table :data="roleList" border stripe>
          <!--展开列-->
          <el-table-column type="expand">
            <template slot-scope="scope">
              <el-row :class="['bdbottom', i1 === 0 ? 'bdtop':'', 'vcenter']" v-for="(item1, i1) in scope.row.children" :key="item1.id">
                <!--渲染一级权限-->
                <el-col :span="5">
                  <el-tag closable @close="removeRightById(scope.row,item1)">{{item1.authName}}</el-tag>
                  <i class="el-icon-arrow-right"></i>
                </el-col>
                <!--渲染二级和三级权限-->
                <el-col :span="19">
                  <!--通过for 循环 嵌套渲染 二级权限-->
                  <el-row :class="[i2 === 0 ? '':'bdtop', 'vcenter']" v-for="(item2, i2) in item1.children" :key="item2.id">
                    <el-col :span="6">
                      <el-tag type="success" closable @close="removeRightById(scope.row,item2)">{{item2.authName}}</el-tag>
                      <i class="el-icon-arrow-right"></i>
                    </el-col>
                    <el-col :span="18">
                      <!--通过for 循环 嵌套渲染 三级权限-->
                      <el-tag v-for="(item3) in item2.children" :key="item3.id" type="warning" closable @close="removeRightById(scope.row,item3)">{{item3.authName}}</el-tag>
                    </el-col>
                  </el-row>
                </el-col>
              </el-row>
            </template>
          </el-table-column>
          <!--索引列-->
          <el-table-column type="index"></el-table-column>
          <el-table-column label="角色名称" prop="roleName"></el-table-column>
          <el-table-column label="角色描述" prop="roleDesc"></el-table-column>
          <el-table-column label="操作" width="300px">
            <template slot-scope="scope">
              <!--修改按钮-->
                <el-button type="primary" @click="showEditDialog(scope.row.id)" icon="el-icon-edit" size="mini">修改</el-button>
              <!--删除按钮-->
                <el-button type="danger" @click="removeUserById(scope.row)" icon="el-icon-delete" size="mini">删除</el-button>
              <!--分配权限按钮-->
                <el-button type="warning" @click="showSetRightDialog(scope.row)" icon="el-icon-setting" size="mini" >分配权限</el-button>
            </template>
          </el-table-column>
        </el-table>
      </el-card>
      <!--分配权限的对话框-->
      <el-dialog title="分配权限" :visible.sync="setRightDiglogVisible" width="50%" @close="setRightDialogClosed">
        <!--树形控件-->
        <el-tree :data="rightsList" :props="treeProps" show-checkbox node-key="id" default-expand-all :default-checked-keys="defKeys" ref="treeRef"></el-tree>
        <!--底部区域-->
        <div slot="footer" class="dialog-footer">
          <el-button @click="setRightDiglogVisible = false">取 消</el-button>
          <el-button type="primary" @click="allotRights">确 定</el-button>
        </div>
      </el-dialog>
    </div>
</template>

<script>
export default {
  data () {
    return {
      roleList: [],
      setRightDiglogVisible: false,
      rightsList: [],
      // 树形控件的属性绑定对象
      treeProps: {
        label: 'authName',
        children: 'children'
      },
      // 默认选中的节点数组
      defKeys: [],
      // 当前即将分配权限的角色id
      roleId: ''
    }
  },
  created () {
    this.getRoleList()
  },
  methods: {
    async getRoleList () {
      const { data: res } = await this.$http.get('roles')
      if (res.meta.status !== 200) {
        return this.$message.error('获取角色列表失败!')
      }
      this.roleList = res.data
    },
    async removeRightById (role, right) {
      // 弹框提示用户是否删除
      const confirmResult = await this.$confirm('此操作将永久删除[ ' + right.authName + ' ]权限, 是否继续?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).catch(err => err)
      if (confirmResult !== 'confirm') {
        return this.$message.info('取消删除权限!')
      }
      const { data: res } = await this.$http.delete(`roles/${role.id}/rights/${right.id}`)
      if (res.meta.status !== 200) {
        return this.$message.error('删除权限失败!')
      }
      this.$message.success('删除权限成功!')
      // this.getRoleList()
      role.children = res.data
    },
    async showSetRightDialog (role) {
      this.roleId = role.id
      // 获取所有权限的数据
      const { data: res } = await this.$http.get('rights/tree')
      if (res.meta.status !== 200) {
        return this.$message.error('获取权限数据失败!')
      }
      this.rightsList = res.data
      // 递归获取三级权限的id
      this.getLeafKeys(role, this.defKeys)
      // 展示 添加权限的对话框
      this.setRightDiglogVisible = true
    },
    // 通过递归的形式, 获取角色下所有三级权限的id , 保存到defkeys 中
    getLeafKeys (node, arr) {
      // 如果当前节点中不包含children属性, 则说明是三级节点
      if (!node.children) {
        return arr.push(node.id)
      }
      node.children.forEach(item => this.getLeafKeys(item, arr))
    },
    // 监听 分配权限对话框的关闭事件
    setRightDialogClosed () {
      this.defKeys = []
    },
    // 分配权限的方法
    async allotRights () {
      // 获取所有的key = 选中的key + 半选的key
      const keys = [
        ...this.$refs.treeRef.getCheckedKeys(),
        ...this.$refs.treeRef.getHalfCheckedKeys()
      ]
      // 转成 字符串, 用 逗号分隔
      const idStr = keys.join(',')
      const { data: res } = await this.$http.post(`roles/${this.roleId}/rights`, { rids: idStr })
      if (res.meta.status !== 200) {
        return this.$message.error('分配权限失败!')
      }
      this.$message.success('分配权限成功!')
      this.getRoleList()
      this.setRightDiglogVisible = false
    }
  }
}
</script>

